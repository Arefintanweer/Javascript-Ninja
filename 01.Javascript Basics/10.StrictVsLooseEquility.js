let age = 25;

//loose equility(different types can be equal)
console.log(age==25);
console.log(age=='25');
console.log(age!=25);
console.log(age!='25');

//Strict equility(different types can not be equal)
console.log(age===25);
console.log(age==='25');
console.log(age!==25);
console.log(age!=='25');

//its better to use Strict equility.

